package com.nomad.entity;

public class Book {
	private int bookNo;
	private String bookName;
	private String bookAuthor;
	private String bookPublishment;
	private int bookQuantity;
	/**
	 * @return the bookNo
	 */
	public int getBookNo() {
		return bookNo;
	}
	/**
	 * @param bookNo the bookNo to set
	 */
	public void setBookNo(int bookNo) {
		this.bookNo = bookNo;
	}
	/**
	 * @return the bookName
	 */
	public String getBookName() {
		return bookName;
	}
	/**
	 * @param bookName the bookName to set
	 */
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	/**
	 * @return the bookAuthor
	 */
	public String getBookAuthor() {
		return bookAuthor;
	}
	/**
	 * @param bookAuthor the bookAuthor to set
	 */
	public void setBookAuthor(String bookAuthor) {
		this.bookAuthor = bookAuthor;
	}
	/**
	 * @return the bookPublishment
	 */
	public String getBookPublishment() {
		return bookPublishment;
	}
	/**
	 * @param bookPublishment the bookPublishment to set
	 */
	public void setBookPublishment(String bookPublishment) {
		this.bookPublishment = bookPublishment;
	}
	/**
	 * @return the bookQuantity
	 */
	public int getBookQuantity() {
		return bookQuantity;
	}
	/**
	 * @param bookQuantity the bookQuantity to set
	 */
	public void setBookQuantity(int bookQuantity) {
		this.bookQuantity = bookQuantity;
	}
	
	
}
